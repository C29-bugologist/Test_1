vowels = 0
count_vowles = input("Count vowels of a sentence")
for i in count_vowles:
    if i == "a":
        vowels += 1
    elif i == "e":
        vowels += 1
    elif i == "i":
        vowels += 1
    elif i == "o":
        vowels += 1
    elif i == "u":
        vowels += 1
    else:
        None

print(vowels)